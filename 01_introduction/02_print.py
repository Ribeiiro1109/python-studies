# A função print() exibe valores no terminal.
# Quando vários valores são passados, eles são separados por um espaço por padrão.
print(12, 13)

# O parâmetro 'sep' (separator) altera o separador entre os valores.
# Neste exemplo, os números serão separados por um hífen (-).
print(9, 10, 11, sep='-')