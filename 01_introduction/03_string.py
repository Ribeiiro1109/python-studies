"""
Python - Strings

Python é uma linguagem de programação com tipagem dinâmica e forte.

str (string) é o tipo de dado utilizado para representar textos.
Uma string pode ser delimitada por aspas simples ('') ou aspas duplas ("").
"""

# String delimitada por aspas simples.
print('João Victor')

# String delimitada por aspas duplas.
print("João Victor")

# O caractere de escape (\) permite incluir aspas do mesmo tipo
# utilizadas para delimitar a string.
print("João \"Victor\"")

# O prefixo 'r' (raw string) faz com que as barras invertidas (\)
# sejam interpretadas literalmente, sem escapar caracteres.
print(r"João \"Victor\"")

# Também é possível utilizar aspas simples dentro de uma string
# delimitada por aspas duplas sem a necessidade de escape.
print("João 'Victor'")


