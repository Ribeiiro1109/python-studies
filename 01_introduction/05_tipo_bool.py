"""
Python - Tipo de dado bool (boolean)

O tipo bool representa valores lógicos e possui apenas dois resultados
possíveis:

- True  -> Verdadeiro
- False -> Falso

Esse tipo é muito utilizado em comparações e estruturas de decisão.
"""

# O operador de igualdade (==) verifica se dois valores são iguais.
# O resultado da comparação será sempre True ou False.

print(10 == 10)  # True
print(10 == 11)  # False

# A função type() retorna o tipo do valor informado.
# Como o resultado de uma comparação é um valor booleano,
# o tipo retornado será bool.

print(type(0 == 0))
