"""
Python - Variáveis

Variáveis são utilizadas para armazenar valores na memória durante
a execução do programa.

O operador de atribuição (=) associa um valor a um nome (variável).

Sintaxe:
nome_da_variavel = valor
"""

# Convenção da PEP 8:
# - Utilize letras minúsculas para nomes de variáveis.
# - Separe palavras com underline (_), quando necessário.
# - Escolha nomes claros e descritivos para facilitar a leitura do código.

nome = "João Victor"
idade = 20
maior_de_idade = idade >= 18

# Exibe os valores armazenados nas variáveis.
print("Nome:", nome)
print("Idade:", idade)
print("Maior de idade?", maior_de_idade)