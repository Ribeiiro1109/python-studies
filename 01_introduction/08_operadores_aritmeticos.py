"""
Python - Operadores Aritméticos

Os operadores aritméticos são utilizados para realizar operações
matemáticas entre valores numéricos.
"""

# Operador de adição (+): soma dois valores.
adicao = 10 + 10
print("Adição:", adicao)

# Operador de subtração (-): subtrai um valor do outro.
subtracao = 10 - 5.0
print("Subtração:", subtracao)

# Operador de multiplicação (*): multiplica dois valores.
multiplicacao = 10 * 3
print("Multiplicação:", multiplicacao)

# Operador de divisão (/): retorna um resultado do tipo float.
divisao = 10 / 2.2
print("Divisão:", divisao)

# Operador de divisão inteira (//): retorna apenas a parte inteira
# do resultado da divisão.
divisao_inteira = 10 // 2.2
print("Divisão inteira:", divisao_inteira)

# Operador de exponenciação (**): eleva um número a uma potência.
exponenciacao = 2 ** 10
print("Exponenciação:", exponenciacao)

# Operador de módulo (%): retorna o resto da divisão entre dois valores.
modulo = 55 % 5
print("Módulo:", modulo)
