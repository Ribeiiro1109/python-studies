"""
Python - Concatenação e Repetição de Strings

Strings podem ser combinadas utilizando operadores.

- O operador (+) realiza a concatenação de strings.
- O operador (*) permite repetir uma string várias vezes.
"""

# O operador (+) une duas ou mais strings em uma única string.
concatenacao = "João" + " " + "Victor"
print(concatenacao)

# O operador (*) repete uma string a quantidade de vezes informada.
a_dez_vezes = "A" * 10
tres_vezes_joao = 3 * "João"

print(a_dez_vezes)
print(tres_vezes_joao)