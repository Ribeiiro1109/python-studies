"""
Python - Operadores de Comparação (Relacionais)

# Os operadores de comparação são muito utilizados em estruturas
# condicionais, como if, elif e while, para tomar decisões
# durante a execução do programa.

Os operadores de comparação são utilizados para comparar dois valores.
O resultado de uma comparação será sempre um valor booleano (True ou False).

Operador    Significado              Exemplo
>           Maior que                2 > 1
>=          Maior ou igual a         2 >= 2
<           Menor que                1 < 2
<=          Menor ou igual a         2 <= 2
==          Igual a                  "a" == "a"
!=          Diferente de             "a" != "b"
"""

# ==========================================================
# Exemplos de operadores de comparação
# ==========================================================

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = "a" == "a"
diferente = "a" != "b"

# Exibe o resultado de cada comparação.
print("Maior que:", maior)
print("Maior ou igual a:", maior_ou_igual)
print("Menor que:", menor)
print("Menor ou igual a:", menor_ou_igual)
print("Igual a:", igual)
print("Diferente de:", diferente)
